import { assert, it } from "vitest";
import { monthEvents } from "./monthEvents.js";

it("monthEvents", () => {
    assert.deepEqual(
        monthEvents(
            [
                { d: "2024-02-26", freq: "1D" },
                { d: "2024-02-23", freq: "2D" },
                { d: "2024-01-20", freq: "1W" },
                { d: "2024-01-21", freq: "1M" },
                { d: "2023-11-22", freq: "3M" },
                { d: "2023-08-23", freq: "6M" },
                { d: "2023-02-24", freq: "1Y" },
                { d: "2022-02-25", freq: "2Y" },
            ],
            2024,
            2,
        ),
        [
            ["2024-02-26", "2024-02-27", "2024-02-28", "2024-02-29"],
            ["2024-02-23", "2024-02-25", "2024-02-27", "2024-02-29"],
            ["2024-02-03", "2024-02-10", "2024-02-17", "2024-02-24"],
            ["2024-02-21"],
            ["2024-02-22"],
            ["2024-02-23"],
            ["2024-02-24"],
            ["2024-02-25"],
        ],
    );
});
